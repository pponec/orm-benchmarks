package org.benchmark.common;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.SQLException;

/** Database utility for benchmark DB connections (H2 / PostgreSQL). */
public class DatabaseUtils {

    private static boolean initialized = false;
    private static String initializedUrl = "";

    private static final String DB_PROFILE_ENV = "BENCHMARK_DB_PROFILE";
    private static final String DB_URL_ENV = "BENCHMARK_DB_URL";
    private static final String DB_USER_ENV = "BENCHMARK_DB_USER";
    private static final String DB_PASSWORD_ENV = "BENCHMARK_DB_PASSWORD";

    private static final String APP_DB_NAME_ENV = "APP_DB_NAME";
    private static final String APP_DB_USER_ENV = "APP_DB_USER";
    private static final String APP_DB_PASSWORD_ENV = "APP_DB_PASSWORD";

    private static final String DEFAULT_H2_URL =
            "jdbc:h2:mem:benchmark;DB_CLOSE_DELAY=-1;MODE=PostgreSQL;DATABASE_TO_LOWER=TRUE;DEFAULT_NULL_ORDERING=HIGH";

    private static final String DDL = """
            CREATE TABLE IF NOT EXISTS city
               ( id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
               , name VARCHAR(50) NOT NULL
               , country_code VARCHAR(2) NOT NULL
               , latitude DECIMAL(10, 8) NOT NULL
               , longitude DECIMAL(11, 8) NOT NULL
               , created_at TIMESTAMP NOT NULL
               , updated_at TIMESTAMP NOT NULL
               , created_by VARCHAR(50) NOT NULL
               , updated_by VARCHAR(50) NOT NULL
           );
            CREATE TABLE IF NOT EXISTS employee
               ( id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
               , name VARCHAR(50) NOT NULL
               , superior_id BIGINT NULL
               , city_id BIGINT NOT NULL
               , contract_day DATE NULL
               , is_active BOOLEAN DEFAULT true
               , email VARCHAR(100) NULL
               , phone VARCHAR(20) NULL
               , salary DECIMAL(10, 2) NULL
               , department VARCHAR(50) NULL
               , created_at TIMESTAMP NOT NULL
               , updated_at TIMESTAMP NOT NULL
               , created_by VARCHAR(50) NOT NULL
               , updated_by VARCHAR(50) NOT NULL
               , CONSTRAINT fk_employee_city FOREIGN KEY (city_id) REFERENCES city(id)
               , CONSTRAINT fk_employee_superior FOREIGN KEY (superior_id) REFERENCES employee(id)
            );
            """;

    private static final String CITY_SEED = """
            INSERT INTO city (id, name, country_code, latitude, longitude, created_at, updated_at, created_by, updated_by)
            VALUES (1, 'Prague', 'CZ', 50.0755, 14.4378, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP, 'System', 'System');
            """;

    /** Provides an initialized benchmark DB connection. */
    public static Connection getConnection() throws SQLException {
        var url = getJdbcUrl();
        ensureDriverLoaded();
        var result = DriverManager.getConnection(url, getJdbcUser(), getJdbcPassword());
        result.setAutoCommit(false);

        if (!initialized || !url.equals(initializedUrl)) {
            synchronized (DatabaseUtils.class) {
                if (!initialized || !url.equals(initializedUrl)) {
                    executeSqlBatch(result, DDL);
                    executeSqlBatch(result, CITY_SEED);
                    result.commit();
                    initialized = true;
                    initializedUrl = url;
                }
            }
        }
        return result;
    }

    /** Resets benchmark data to deterministic initial state for current DB profile. */
    public static void resetDatabase() {
        try {
            ensureDriverLoaded();
        } catch (SQLException e) {
            throw new RuntimeException("Failed to load JDBC driver for profile: " + getDbProfile(), e);
        }
        try (var connection = DriverManager.getConnection(getJdbcUrl(), getJdbcUser(), getJdbcPassword());
             var statement = connection.createStatement()) {
            connection.setAutoCommit(false);
            statement.execute("DROP TABLE IF EXISTS employee CASCADE");
            statement.execute("DROP TABLE IF EXISTS city CASCADE");
            executeSqlBatch(connection, DDL);
            executeSqlBatch(connection, CITY_SEED);
            connection.commit();
            initialized = true;
            initializedUrl = getJdbcUrl();
        } catch (SQLException e) {
            throw new RuntimeException("Failed to reset benchmark database.", e);
        }
    }

    public static String getDbProfile() {
        var systemValue = System.getProperty("benchmark.db.profile");
        if (systemValue != null && !systemValue.isBlank()) {
            return systemValue.trim().toLowerCase();
        }
        var envValue = System.getenv(DB_PROFILE_ENV);
        if (envValue != null && !envValue.isBlank()) {
            return envValue.trim().toLowerCase();
        }
        return "h2";
    }

    public static boolean isPostgresProfile() {
        return "postgres".equals(getDbProfile()) || "postgresql".equals(getDbProfile());
    }

    public static String getJdbcUrl() {
        var systemValue = System.getProperty("benchmark.db.url");
        if (systemValue != null && !systemValue.isBlank()) {
            return systemValue.trim();
        }
        var envValue = System.getenv(DB_URL_ENV);
        if (envValue != null && !envValue.isBlank()) {
            return envValue.trim();
        }
        if (isPostgresProfile()) {
            var dbName = envOrDefault(APP_DB_NAME_ENV, "benchmark");
            return "jdbc:postgresql://localhost:5432/" + dbName;
        }
        return DEFAULT_H2_URL;
    }

    public static String getJdbcUser() {
        var systemValue = System.getProperty("benchmark.db.user");
        if (systemValue != null) {
            return systemValue;
        }
        var envValue = System.getenv(DB_USER_ENV);
        if (envValue != null) {
            return envValue;
        }
        return isPostgresProfile() ? envOrDefault(APP_DB_USER_ENV, "benchmark") : "sa";
    }

    public static String getJdbcPassword() {
        var systemValue = System.getProperty("benchmark.db.password");
        if (systemValue != null) {
            return systemValue;
        }
        var envValue = System.getenv(DB_PASSWORD_ENV);
        if (envValue != null) {
            return envValue;
        }
        return isPostgresProfile() ? envOrDefault(APP_DB_PASSWORD_ENV, "benchmark") : "";
    }

    private static String envOrDefault(String key, String defaultValue) {
        var value = System.getenv(key);
        return value == null || value.isBlank() ? defaultValue : value;
    }

    private static void executeSqlBatch(Connection connection, String sqlBatch) throws SQLException {
        try (var statement = connection.createStatement()) {
            for (var sql : sqlBatch.split(";")) {
                var trimmed = sql.trim();
                if (!trimmed.isEmpty()) {
                    statement.execute(trimmed);
                }
            }
        }
    }

    private static void ensureDriverLoaded() throws SQLException {
        var driverClass = isPostgresProfile() ? "org.postgresql.Driver" : "org.h2.Driver";
        try {
            Class.forName(driverClass);
        } catch (ClassNotFoundException e) {
            throw new SQLException("Missing JDBC driver class: " + driverClass, e);
        }
    }
}